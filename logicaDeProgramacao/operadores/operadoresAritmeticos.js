/*
 Operadores aritméticos:
 +      adição
 -      subtração
 *      multiplicação
 /      divisão
 **     potencia
 ++     incremento  seria a mesma coisa que     x = x + 1
 --     decremento  seria a mesma coisa que     y = y - 1
*/
let num1 = 4
let num2 = 3

//adição
console.log(" ")
console.log("adição")
console.log(`${num1} + ${num2} = ${num1 + num2}`)
//subtração
console.log(" ")
console.log("subtração")
console.log(`${num1} - ${num2} = ${num1 - num2}`)
//multiplicação
console.log(" ")
console.log("multiplicação")
console.log(`${num1} * ${num2} = ${num1 * num2}`)
//divisão
console.log(" ")
console.log("divisão")
console.log(`${num1} / ${num2} = ${num1 / num2}`)
//módulo(resto de uma operação de divisão)
console.log(" ")
console.log("módulo")
console.log(`${num1} % ${num2} = ${num1 % num2}`)
//potencia
console.log(" ")
console.log("potencia")
console.log(`${num1} ** ${num2} = ${num1 ** num2}`)
//decremento
console.log(" ")
console.log("decremento")
console.log(`--${num1} = ${--num1}`)
//incremento
console.log(" ")
console.log("incremento")
console.log(`++${num1} = ${++num1}`)

// usar o ++ ou --, antes da variavel executa antes e vice-versa