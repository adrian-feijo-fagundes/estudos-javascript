// OPERADORES LÓGICOS

// &&   conjunção       e 
// ||   disjunção       ou
// !    negação         não

console.log('Operador !')
console.log('!true: ' +(!true))
console.log('!false: ' +(!false))

console.log('Operador &&')
console.log('false && false: ' +(false && false))
console.log('false && true: ' +(false && true))
console.log('true && false: ' +(true && false))
console.log('true && true: ' +(true && true))

console.log('Operador ||')
console.log('false || false: ' + (false || false))
console.log('false || true: ' + (false || true))
console.log('true || false: ' +(true || false))
console.log('true || true: ' +(true || true))