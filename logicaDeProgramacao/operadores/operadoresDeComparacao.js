// COMPARAÇÃO

//  ==      Igual a
//  ===     Igual a (tanto valor quanto o tipo)
//  !=      Diferente de
//  !==     Diferente de (tanto valor quanto o tipo)
//  >       Maior que
//  >=      Maior ou igual a
//  <       Menor
//  <=      Menor ou igual a
//EXEMPLOS:
console.log('Operador ==')
console.log("5 é igual a 5? " + (5 == 5))
console.log("5 é igual a '5'? " + (5 == '5'))
console.log("5 é igual a 4? " + (5 == 4))

console.log('Operador ===')
console.log("5 é igual a '5'? " + (5 === '5'))
console.log("5 é igual a 5? " + (5 === 5))

console.log('Operador !=')
console.log("5 é diferente de 5? " + (5 != 5))
console.log("5 é diferente de 4? " + (5 != 4))

console.log('Operador >')
console.log("5 é maior que 6? " + (5 > 6))
console.log("5 é maior que 4? " + (5 > 4))

console.log('Operador <')
console.log("5 é menor que 6? " + (5 < 6))
console.log("5 é menor que 4? " + (5 < 4))

console.log('Operador >=')
console.log("5 é maior ou igual 6? " + (5 >= 6))
console.log("5 é maior ou igual a 4? " + (5 >= 4))
console.log("5 é maior ou igual a 5? " + (5 >= 5))

console.log('Operador <=')
console.log("5 é menor ou igual a 6? " + (5 <= 6))
console.log("5 é menor ou igual a 4? " + (5 <= 4))
console.log("5 é menor ou igual a 5? " + (5 <= 5))